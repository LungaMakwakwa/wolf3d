#include "libft.h"

void	ft_strclr(char *s)
{
	size_t len;
	size_t i;

	i = 0;
	if (!s)
	{
		return ;
	}
	len = ft_strlen(s);
	while (len > i)
	{
		s[i] = '\0';
		i++;
	}
}
