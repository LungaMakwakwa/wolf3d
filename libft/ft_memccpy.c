#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	*dest;
	unsigned char	*source;
	unsigned char	chr;
	size_t			i;

	i = 0;
	chr = (unsigned char)c;
	source = (unsigned char *)src;
	dest = (unsigned char *)dst;
	while (i < n)
	{
		dest[i] = source[i];
		if (source[i] == chr)
		{
			return (dst + i + 1);
		}
		i++;
	}
	return (NULL);
}
